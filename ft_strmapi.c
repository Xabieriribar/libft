#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*new_str;
	size_t			size;
	unsigned int	index;

	if (!s || !f)
		return (NULL);
	size = ft_strlen(s);
	new_str = malloc(size + 1);
	if (!new_str)
		return (NULL);
	index = 0;
	while (index < size)
	{
		new_str[index] = f(index, s[index]);
		index++;
	}
	new_str[index] = '\0';
	return (new_str);
}
